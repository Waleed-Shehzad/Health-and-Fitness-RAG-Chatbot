import fitz
import re
from fpdf import FPDF


def clean_text(text):
    text = re.sub(r'[^\x00-\x7F]+', ' ', text)
    text = re.sub(r'\s+', ' ', text)
    return text.strip()

class PDF(FPDF):
    def header(self):
        self.set_font("Arial", "B", 12)

    def chapter_body(self, text):
        self.set_font("Arial", "", 11)
        self.multi_cell(0, 10, text)

# Extract text, clean it, and write to PDF
def create_cleaned_pdf(input_pdf_path, output_pdf_path):
    # Load and extract text
    doc = fitz.open(input_pdf_path)
    cleaned_text = ""
    for page in doc:
        text = page.get_text()
        cleaned_text += clean_text(text) + "\n\n"

    # Create PDF
    pdf = PDF()
    pdf.add_page()
    pdf.set_auto_page_break(auto=True, margin=15)
    pdf.chapter_body(cleaned_text)
    pdf.output(output_pdf_path)
    print(f"Cleaned PDF saved as: {output_pdf_path}")

# Run the function
create_cleaned_pdf("/content/Emerge.pdf", "Cleaned_English.pdf")

